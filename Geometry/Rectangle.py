"""To find Area and Circumference of the rectangle
"""

def get_rect_area(length,breadth):
    """Compute and return area of rectangle
    """

    area = length * breadth
    return area



def get_rect_circum(length,breadth):
    """Compute and return circumference of rectangle
    """

    circum = 2 * (length + breadth)
    return circum
